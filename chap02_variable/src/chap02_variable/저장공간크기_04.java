package chap02_variable;

public class 저장공간크기_04 {

	public static void main(String[] args) { 
		System.out.println("byte크기 = " + Byte.MIN_VALUE + "~" + Byte.MAX_VALUE);
		System.out.println("short크기 = " + Short.MIN_VALUE + "~" + Short.MAX_VALUE);
		System.out.println("int크기 =" + Integer.MIN_VALUE + "~" + Integer.MIN_VALUE);
		System.out.println("long크기 = " + Long.MIN_VALUE + "~" + Long.MIN_VALUE);

		System.out.println("char크기 = " + (int)Character.MIN_VALUE + "~" + (int)Character.MIN_VALUE);
	}

}
